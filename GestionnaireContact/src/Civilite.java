

public class Civilite {
	private String Monsieur;
	private String Madame;
	private String Docteur;
	
	
	public String getMonsieur() {
		return Monsieur;
	}
	public void setMonsieur(String monsieur) {
		Monsieur = monsieur;
	}
	public String getMadame() {
		return Madame;
	}
	public void setMadame(String madame) {
		Madame = madame;
	}
	public String getDocteur() {
		return Docteur;
	}
	public void setDocteur(String docteur) {
		Docteur = docteur;
	}
	
	
	

}
